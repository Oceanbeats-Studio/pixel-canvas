/* ===============================
   NAVIGATION
   =============================== */
prevBtn.onclick = () => {
  currentIndex = (currentIndex - 1 + filteredIdeas.length) % filteredIdeas.length;
  drawPreview(filteredIdeas[currentIndex]);
};

nextBtn.onclick = () => {
  currentIndex = (currentIndex + 1) % filteredIdeas.length;
  drawPreview(filteredIdeas[currentIndex]);
};

/* ===============================
   FILTER BY SIZE
   =============================== */
function updateGalleryForSize(size) {
  // 32×32 = free draw
  if (size === 32) {
    gallery.style.display = "none";
    return;
  }

  filteredIdeas = galleryData.filter(item => item.size === size);

  if (!filteredIdeas.length) {
    gallery.style.display = "none";
    return;
  }

  gallery.style.display = "block";
  currentIndex = 0;
  drawPreview(filteredIdeas[0]);
}

window.updateGalleryForSize = updateGalleryForSize;
